class _PolicyBody extends StatelessWidget {
  const _PolicyBody({required this.text});

  final String text;

  bool _isMainSectionTitle(String line) {
    final t = line.trimLeft();
    return t.startsWith('1. ') ||
        t.startsWith('2. ') ||
        t.startsWith('3. ') ||
        t.startsWith('4. ') ||
        t.startsWith('5. ') ||
        t.startsWith('6. ') ||
        t.startsWith('7. ') ||
        t.startsWith('8. ') ||
        t.startsWith('9. ') ||
        t.startsWith('10. ') ||
        t.startsWith('11. ');
  }

  @override
  Widget build(BuildContext context) {
    final lines = text.split('\n');

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        for (final line in lines)
          if (line.trim().isNotEmpty)
            Padding(
              padding: EdgeInsets.only(
                top: _isMainSectionTitle(line) ? 30 : 20,
                left: _isMainSectionTitle(line) ? 0 : 10,
              ),
              child: Text(
                line,
                style: _isMainSectionTitle(line)
                    ? AppTextStyles.unboundedRegular18.copyWith(
                        color: Colors.black87,
                      )
                    : AppTextStyles.body14.copyWith(
                        fontSize: 16,
                      ),
              ),
            ),
      ],
    );
  }
